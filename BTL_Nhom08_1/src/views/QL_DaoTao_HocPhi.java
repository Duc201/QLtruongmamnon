 public String CovertTimeDDMMYYYY(Date date1){
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        
        return dateFormat.format(date1); 
    }
  public void capNhatBang(){
          if (tableModel == null) {
        taoBang();
    }
        tableModel.setRowCount(0);
       
        String namHoc = comboBoxNienKhoa.getSelectedItem().toString();
        System.out.println(namHoc);
        listHocPhi = new Controller_HocPhi().getListHocPhiFollowNamHoc(namHoc);
        for(hocPhi hp : listHocPhi){
            Object[] rowData = {hp.getTenHocPhi(), CovertTimeDDMMYYYY(hp.getNgayBatDau()), CovertTimeDDMMYYYY(hp.getNgayKetThuc()), hp.getHocPhi1Bua(), hp.getMaNienKhoa(), hp.getMaHocPhi() };
            tableModel.addRow(rowData);
        }
    }
    public void themHocPhi(){
        hocPhi hp = new hocPhi();
        hp.setTenHocPhi(txtTenHocPhi.getText());
        hp.setNgayBatDau(dateChooserBD.getDate());
        hp.setNgayKetThuc(datechooserKT.getDate());
        hp.setHocPhi1Bua(Double.parseDouble(txtTienMotBuoi.getText()));
        String maNienKhoa = new Controller_QlDsLop().layMaNienKhoa(comboBoxNienKhoa.getSelectedItem().toString());
        hp.setMaNienKhoa(maNienKhoa);
        new Controller_HocPhi().themHocPhi(hp);
        capNhatBang();
        //clearText();
    }
    public void capNhatHocPhi(){
        hocPhi hp = new hocPhi();
        hp.setMaHocPhi( Integer.parseInt(tableModel.getValueAt(selecRow, 5).toString()));
        hp.setTenHocPhi(txtTenHocPhi.getText());
        hp.setNgayBatDau(dateChooserBD.getDate());
        hp.setNgayKetThuc(datechooserKT.getDate());
        hp.setHocPhi1Bua(Double.parseDouble(txtTienMotBuoi.getText()));
        String maNienKhoa = new Controller_QlDsLop().layMaNienKhoa(comboBoxNienKhoa.getSelectedItem().toString());
        hp.setMaNienKhoa(maNienKhoa);
        new Controller_HocPhi().capNhatHocPhi(hp);
        capNhatBang();
        //clearText();
    }
        public void deleteHocPhi(){
        selecRow = table.getSelectedRow();
        String maHocPhi = "";
        if(selecRow != -1){
        maHocPhi = tableModel.getValueAt(selecRow, 5).toString();
        
        int option = JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa học phí không ? ", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
        if(option == JOptionPane.YES_OPTION){
            new Controller_HocPhi().deleteHocPhi(maHocPhi);
        }
        }
        else
            JOptionPane.showMessageDialog(null, "Vui lòng chọn một Học Phí để xóa");
        
       capNhatBang();
        
    }
