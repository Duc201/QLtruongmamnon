    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
      
        selecRow = table.getSelectedRow();
     
     if(selecRow != -1){
         txtTenHocPhi.setText(tableModel.getValueAt(selecRow, 0).toString());
                    SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
            try {
                Date date = dateFormat.parse(tableModel.getValueAt(selecRow, 1).toString()); // Chuyen STring date(dd/mm/year)  ve dang Date
                dateChooserBD.setDate(date);
                Date date1 = dateFormat.parse(tableModel.getValueAt(selecRow, 2).toString()); // Chuyen STring date(dd/mm/year)  ve dang Date
                datechooserKT.setDate(date1);
            } catch (ParseException e) {
                e.printStackTrace();
                dateChooserBD.setDate(new Date()); // Nếu không thể chuyển đổi, đặt ngày hiện tại
                datechooserKT.setDate(new Date());
            }
            
         txtTienMotBuoi.setText(tableModel.getValueAt(selecRow, 3).toString());
     }
    }//GEN-LAST:event_tableMouseClicked

